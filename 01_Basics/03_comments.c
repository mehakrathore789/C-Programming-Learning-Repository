/*
 * Program: Comments in C
 * Description: Comments are ignored by the compiler and are used to explain code.
 */

#include <stdio.h>

// This is a single-line comment. It explains the next line of code.
int main(void) {

    /*
     * This is a multi-line comment.
     * It can span across several lines.
     * Useful for longer explanations or temporarily disabling code blocks.
     */

    printf("Comments help make code readable!\n"); // Comment at the end of a line

    return 0;
}
